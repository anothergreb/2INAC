a = int(input("a: "))
b = int(input("b: "))

soma = a + b
diferenca = a - b
produto = a * b
divisao = a / b
divisao_inteira = a // b
resto = a % b

print(f"Soma: {soma}")
print(f"Diferença: {diferenca}")
print(f"Produto: {produto}")
print(f"Divisão: {divisao:.2f}")
print(f"Divisão Inteira: {divisao_inteira}")
print(f"Resto: {resto}")